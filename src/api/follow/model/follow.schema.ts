import mongoose from "mongoose";

const followSchema = new mongoose.Schema<IFollow>({
  from: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  to: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export const MongooseFollow = mongoose.model<IFollow>("Follow", followSchema);
